def menu_agendamentos():
    while True:
        print("\n--- MENU AGENDAMENTOS ---")
        print("1 - Criar Agendamento")
        print("2 - Buscar Agendamento")
        print("3 - Listar Agendamentos")
        print("4 - Cancelar Agendamento")
        print("0 - Voltar")

        opcao = input("Escolha: ")

        if opcao == "1":
            print("Criar agendamento - EM DESENVOLVIMENTO")
        elif opcao == "2":
            print("Buscar agendamento - EM DESENVOLVIMENTO")
        elif opcao == "3":
            print("Listar agendamentos - EM DESENVOLVIMENTO")
        elif opcao == "4":
            print("Cancelar agendamento - EM DESENVOLVIMENTO")
        elif opcao == "0":
            break
        else:
            print("Opção inválida!")